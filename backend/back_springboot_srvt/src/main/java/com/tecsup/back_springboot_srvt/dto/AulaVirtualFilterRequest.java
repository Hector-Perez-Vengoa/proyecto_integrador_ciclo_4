package com.tecsup.back_springboot_srvt.dto;

public class AulaVirtualFilterRequest {
    
    private String codigo;
    private String descripcion;
    private String fecha;
    private String horaInicio;
    private String horaFin;
    private Long cursoId;

    // Constructors
    public AulaVirtualFilterRequest() {}

    public AulaVirtualFilterRequest(String codigo, String descripcion, String fecha, 
                                  String horaInicio, String horaFin, Long cursoId) {
        this.codigo = codigo;
        this.descripcion = descripcion;
        this.fecha = fecha;
        this.horaInicio = horaInicio;
        this.horaFin = horaFin;
        this.cursoId = cursoId;
    }

    // Getters and Setters
    public String getCodigo() {
        return codigo;
    }

    public void setCodigo(String codigo) {
        this.codigo = codigo;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    public String getFecha() {
        return fecha;
    }

    public void setFecha(String fecha) {
        this.fecha = fecha;
    }

    public String getHoraInicio() {
        return horaInicio;
    }

    public void setHoraInicio(String horaInicio) {
        this.horaInicio = horaInicio;
    }

    public String getHoraFin() {
        return horaFin;
    }

    public void setHoraFin(String horaFin) {
        this.horaFin = horaFin;
    }

    public Long getCursoId() {
        return cursoId;
    }

    public void setCursoId(Long cursoId) {
        this.cursoId = cursoId;
    }
}
